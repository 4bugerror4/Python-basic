# 1. 비교 연산
print(2 > 3) # false
print(15 < 30) # true
print(1.5 >= 0) # true
print(3 <= 3) # true
print("팙팗팚" == "팙팗팗") # false
print("1111111111111111111" != "1111111111111111111") # false

# 2. 논리 연산
print(4 < 6 and 10 >= 10) # true
print("포기하지말아요" != "포기하지말아요" or "나는 할 수 있다" == "나는 할 수 있다") # true
print(not 5==5) # false

# 3. 멤버십 연산
print("a" in "abc") # 포함되어 있다면 true
print("d" not in "abc") # 포함되어 있지 않다면 true